#include<stdio.h>

int main(){
	int a = 20;
	int b = 5;
	
	// toan tu -
	int hieu = a - b;
	printf("phep tong la %d\n",hieu);
	 
	// toan tu * 
	int tich = a * b;
	printf("phep tong là %d\n",tich);
	
	// phep chia  / 
	int thuong = a  / b;
	printf("phep tong la %d\n",thuong); 
	 
	return 0; 
} 
